        name: 'Редактировать',
        icon: 'account',
    }
]
const sendProp = async () => {
    await sendProposition(1, 1, 1, 'dd', "PROJECT_TO_USER")
}
const complaint = ref('')
const sendComplaint = async () => {
    await postAddComplaint(lastPart.value, localStorage.getItem('userId'), complaint.value,)
}
