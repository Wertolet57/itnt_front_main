    showLogo: {
        type: Boolean,
        default: false,
    },
    showID: {
        type: Boolean,
        default: false,
    },
    showUserMinify: {
        type: Boolean,
        default: false,
    },
    showControlDots: {
        type: Boolean,
        default: false,
    },
    showControlDotsProject: {
        type: Boolean,
        default: false,
    },
